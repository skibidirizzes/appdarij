const flattenAchievements = (achievements: typeof achievementTranslations): FlattenedAchievementTranslations => {
    const flattened = {} as FlattenedAchievementTranslations;
    for (const key in achievements) {
        const id = key as AchievementID;
        (flattened as any)[`achievement_${id}_name`] = achievements[id].name;
        (flattened as any)[`achievement_${id}_description`] = achievements[id].description;
    }
    return flattened;
}

